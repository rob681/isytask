/** Get a random phrase from a category */
export function getRandomPhrase(
  category: keyof typeof MOTIVATIONAL_PHRASES
): string {
  const phrases = MOTIVATIONAL_PHRASES[category];
  return phrases[Math.floor(Math.random() * phrases.length)];
}

/** Get a random client motivational phrase (shown after task creation) */
export function getClientTaskPhrase(): string {
  return getRandomPhrase("clientTaskCreated");
}

/** Get an end-of-day phrase, optionally with task count */
export function getEndOfDayPhrase(completedTasksToday?: number): string {
  if (completedTasksToday && completedTasksToday > 0 && Math.random() > 0.5) {
    const template = getRandomPhrase("withTaskCount");
    return template.replace("{count}", String(completedTasksToday));
  }
  return getRandomPhrase("endOfDay");
}
